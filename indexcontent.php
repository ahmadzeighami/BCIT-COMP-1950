          <h1>COMP 1950</h1>
          <p class="lead">This hands-on course follows on from COMP 1850 Applied Web Development Level 1.</p>
          <p>Students who already understand HTML and CSS will explore web development in more depth with new topics and techniques. Participants will gain a deeper understanding of the latest technologies including HTML5 and CSS3 for developing web sites and applications. There is a focus on responsive design and mobile presentation through the use of Media Queries. Adding behavior to web sites using JavaScript frameworks such as jQuery is introduced. Labs and exercises focus on industry standard web development methodology.</p>

          <p>COMP 1950 is a required course for the BCIT Computing Advanced Web Technologies associate certificates in Applied Web Development, AWD and Web and Mobile Application Development, WMA, as well as an elective in the CST Diploma. COMP 1950 and COMP 2015 are also the prerequisites for COMP 2052 Android and Mobile Application Development Tools. By the end of this course successful students will be able to build dynamic and semantically marked-up content providing Search Engine Optimized (SEO) pages.</p>

          <h3>Course Outcomes</h3>
              <ol>
                  <li>Mark up pages using latest HTML technologies, including HTML5.</li>
                  <li>Utilize CSS to control page layout and presentation, including <li>CSS3.</li>
                  <li>Use media queries to create Responsive designs for mobile and desktop presentation.</li>
                  <li>Add behaviour to any web page using JavaScript.</li>
                  <li>Create dynamic content using JavaScript frameworks like jQuery.</li>
                  <li>Employ W3C Web Standards when building web sites for maximum compatibility.</li>
                  <li>Understand concepts for Search Engine Optimized content (SEO).</li>
                  <li>Install, configure and understand server side architecture using Apache.</li>
                  <li>Understand template based front end development using Bootstrap and HTML Boilerplate.</li>
                  <li>Understand team-based web development methodology.</li>
                  <li>Use HTML5 to present rich content such as video, audio and geolocation.</li>
                  <li>Create professional looking typography using web-based fonts.</li>
                  <li>Understand source control using GIT.</li>
              </ol>
